function Add(a, b) {
    let sum = a + b;
    return sum;
}


function Subtraction(a, b) {
    let sub = a - b;
    return sub;
}

function Multiplication(a, b) {
    let multi = a * b;
    return multi;
}

function Division(a, b) {
    let div = a / b;
    return div;
}

export {
    Add,
    Subtraction,
    Multiplication,
    Division,
};